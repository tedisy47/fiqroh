        <div class="card-body text-justify">
          <p>Zakat adalah harta tertentu yang dikeluarkan apabila telah mencapai syarat yang diatur sesuai aturan agama, 
          dikeluarkan kepada 8 asnaf penerima zakat. Menurut Bahasa kata “zakat” berarti tumbuh, berkembang, subur atau bertambah.</p>
          <p>Zakat berasal dari bentuk kata "zaka" yang berarti suci, baik, berkah, tumbuh, dan berkembang. Dinamakan zakat, 
          karena di dalamnya terkandung harapan untuk beroleh berkah, membersihkan jiwa dan memupuknya dengan berbagai kebaikan 
          (Fikih Sunnah, Sayyid Sabiq: 5)</p>
          <p>Makna tumbuh dalam arti zakat menunjukkan bahwa mengeluarkan zakat sebagai sebab adanya pertumbuhan dan perkembangan harta, 
          pelaksanaan zakat itu mengakibatkan pahala menjadi banyak. Sedangkan makna suci menunjukkan bahwa zakat adalah mensucikan 
          jiwa dari kejelekan, kebatilan dan pensuci dari dosa-dosa.</p>
          <p>Dalam Al-Quran disebutkan, “Ambillah zakat dari sebagian harta mereka, dengan zakat itu kamu membersihkan dan 
          menyucikan mereka” (QS. at-Taubah [9]: 103).</p>
          <p>Menurut istilah dalam kitab al-Hâwî, al-Mawardi mendefinisikan zakat dengan nama pengambilan tertentu dari harta tertentu, 
          menurut sifat-sifat tertentu dan untuk diberikan kepada golongan tertentu. Orang yang menunaikan zakat disebut Muzaki. 
          Sedangkan orang yang menerima zakat disebut Mustahik.</p>
          <p>Sementara menurut Peraturan Menteri Agama No 52 Tahun 2014, Zakat adalah harta yang wajib dikeluarkan oleh seorang 
          muslim atau badan usaha yang dimiliki oleh orang Islam untuk diberikan kepada yang berhak menerimanya sesuai dengan syariat 
          Islam.</p>
          <hr>
          <h6 class="font-weight-bold">Asnaf (8 Golongan) Penerima Zakat</h6>
          <p>Sebagai instrumen yang masuk dalam salah satu Rukun Islam, zakat tentu saja memiliki aturan mengikat dari segi ilmu fiqihnya, 
          salah satu diantaranya adalah kepada siapa zakat diberikan.</p>
          <p>Dalam QS. At-Taubah ayat 60, Allah memberikan ketentuan ada delapan golongan orang yang menerima zakat yaitu sebagai berikut:</p>
          <ol>
            <li><strong>Fakir</strong>, mereka yang hampir tidak memiliki apa-apa sehingga tidak mampu memenuhi kebutuhan pokok hidup.</li>
            <li><strong>Miskin</strong>, mereka yang memiliki harta namun tidak cukup untuk memenuhi kebutuhan dasar kehidupan.</li>
            <li><strong>Amil</strong>, mereka yang mengumpulkan dan mendistribusikan zakat.</li>
            <li><strong>Mualaf</strong>, mereka yang baru masuk Islam dan membutuhkan bantuan untuk menguatkan dalam tauhid dan syariah.</li>
            <li><strong>Riqab</strong>, budak atau hamba sahaya yang ingin memerdekakan dirinya.</li>
            <li><strong>Gharimin</strong>, mereka yang berhutang untuk kebutuhan hidup dalam mempertahankan jiwa dan izzahnya.</li>
            <li><strong>Fisabilillah</strong>, mereka yang berjuang di jalan Allah dalam bentuk kegiatan dakwah, jihad dan sebagainya.</li>
            <li><strong>Ibnu Sabil</strong>, mereka yang kehabisan biaya di perjalanan dalam ketaatan kepada Allah.</li>
          </ol>
          <hr>
          <h6 class="font-weight-bold">Jenis Zakat</h6>
          <p>Secara umum zakat terbagi menjadi dua jenis, yakni zakat fitrah dan zakat mal. Zakat Fitrah (zakat al-fitr) adalah zakat yang 
          diwajibkan atas setiap jiwa baik lelaki dan perempuan muslim yang dilakukan pada bulan Ramadhan.</p>
          <p>Zakat mal adalah zakat yang dikenakan atas segala jenis harta, yang secara zat maupun substansi perolehannya, 
          tidak bertentangan dengan ketentuan agama. Sebagai contoh, zakat mal terdiri atas uang, emas, surat berharga, 
          penghasilan profesi, dan lain-lain, sebagaimana yang terdapat dalam UU No 23/2011 tentang Pengelolaan Zakat, 
          Peraturan Menteri Agama No 52 Tahun 2014 yang telah diubah dua kali dengan perubahan kedua adalah Peraturan Menteri 
          Agama No 31/2019, dan pendapat Syaikh Dr. Yusuf Al-Qardhawi serta para ulama lainnya</p>
          <p>Zakat mal sebagaimana dimaksud pada paragraf di atas meliputi:</p>
          <ol>
            <li><strong>Zakat emas, perak, dan logam mulia lainnya</strong><br>Adalah zakat yang dikenakan atas emas, perak, dan logam lainnya yang telah mencapai nisab dan haul.</li>
            <li><strong>Zakat atas uang dan surat berharga lainnya</strong><br>Adalah zakat yang dikenakan atas uang, harta yang disetarakan dengan uang, dan surat berharga lainnya yang telah mencapai nisab dan haul.</li>
            <li><strong>Zakat perniagaan</strong><br>Adalah zakat yang dikenakan atas usaha perniagaan yang telah mencapai nisab dan haul.</li>
            <li><strong>Zakat pertanian, perkebunan, dan kehutanan</strong><br>Adalah zakat yang dikenakan atas hasil pertanian, perkebunan dan hasil hutan pada saat panen.</li>
            <li><strong>Zakat peternakan dan perikanan</strong><br>Adalah zakat yang dikenakan atas binatang ternak dan hasil perikanan yang telah mencapai nisab dan haul.</li>
            <li><strong>Zakat pertambangan</strong><br>Adalah zakat yang dikenakan atas hasil usaha pertambangan yang telah mencapai nisab dan haul.</li>
            <li><strong>Zakat perindustrian</strong><br>Adalah zakat atas usaha yang bergerak dalam bidang produksi barang dan jasa.</li>
            <li><strong>Zakat pendapatan dan jasa</strong><br>Adalah zakat yang dikeluarkan dari penghasilan yang diperoleh dari hasil profesi pada saat menerima pembayaran, zakat ini dikenal juga sebagai zakat profesi atau zakat penghasilan.</li>
            <li><strong>Zakat rikaz</strong><br>Adalah zakat yang dikenakan atas harta temuan, dimana kadar zakatnya adalah 20%.</li>
          </ol> 
          <hr>
          <h6 class="font-weight-bold">Syarat Zakat Mal dan Zakat Fitrah</h6>
          <ol>
            <li>Harta yang dikenai zakat harus memenuhi syarat sesuai dengan ketentuan syariat Islam.</li>
            <li>Syarat harta yang dikenakan zakat mal sebagai berikut:
                <ol type="a">
                    <li>milik penuh</li>
                    <li>halal</li>
                    <li>cukup nisab</li>
                    <li>haul</li>
                </ol>
            </li>
            <li>Hanya saja, syarat haul tidak berlaku untuk zakat pertanian, perkebunan dan kehutanan, perikanan, pendapatan dan jasa, serta zakat rikaz.</li>
          </ol>
          <p>Sedangkan untuk syarat zakat fitrah sebagai berikut:</p>
          <ol type="a">
            <li>beragama Islam</li>
            <li>hidup pada saat bulan ramadhan</li>
            <li>memiliki kelebihan kebutuhan pokok untuk malam dan hari raya idul fitri</li>
          </ol>
